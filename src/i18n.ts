export function initI18n() {
  const saved = typeof window !== 'undefined' ? (localStorage.getItem('lang') || 'en') : 'en';
  return i18n
    .use(initReactI18next)
    .init({
      resources,
      lng: saved,
      fallbackLng: 'en',
      interpolation: { escapeValue: false }
    });
}

export default i18n;
